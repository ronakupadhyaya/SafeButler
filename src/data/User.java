package data;

public class User {

	private String name;
	private String email;
	private String address;
	private String houseOrCondo;
	private String yearBuilt;
	private String squareFootage;
	private String roofType;
	private String construction;
	private String stories;
	private String swimmingPool;
	private String fixtures;
	private String discounts;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	
	public String getHouseOrCondo() {
		return houseOrCondo;
	}
	public void setHouseOrCondo(String houseOrCondo) {
		this.houseOrCondo = houseOrCondo;
	}
	
	public String getYearBuilt() {
		return yearBuilt;
	}
	public void setYearBuilt(String yearBuilt) {
		this.yearBuilt = yearBuilt;
	}
	
	public String getSquareFootage() {
		return squareFootage;
	}
	public void setSquareFootage(String squareFootage) {
		this.squareFootage = squareFootage;
	}
	
	public String getRoofType() {
		return roofType;
	}
	public void setRoofType(String roofType) {
		this.roofType = roofType;
	}
	
	public String getConstruction() {
		return construction;
	}
	public void setConstruction(String construction) {
		this.construction = construction;
	}
	
	public String getStories() {
		return stories;
	}
	public void setStories(String stories) {
		this.stories = stories;
	}
	
	public String getSwimmingPool() {
		return swimmingPool;
	}
	public void setSwimmingPool(String swimmingPool) {
		this.swimmingPool = swimmingPool;
	}
	
	public String getFixtures() {
		return fixtures;
	}
	public void setFixtures(String fixtures) {
		this.fixtures = fixtures;
	}
	
	public String getDiscounts() {
		return discounts;
	}
	public void setDiscounts(String discounts) {
		this.discounts = discounts;
	}
	
}
